days = 0

# Читаем температуру, пока не встретим 22.0 и больше
while True:
    temp = float(input())  # Считываем температуру
    if temp >= 22.0:  # Если температура больше или равна 22.0
        break  # Прерываем цикл, так как нашли нужную температуру
    days += 1  # Увеличиваем счетчик дней

# Количество полных недель (каждая неделя = 7 дней)
full_weeks = days // 7

# Выводим результат
print(full_weeks)